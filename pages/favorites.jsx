import { useEffect, useState } from "react";
import { getCharacter } from "lib/utils";
import Head from "next/head";
import Grid from "components/Grid";
import Card from "components/Card";

const IMG_SIZE = "standard_large";

const Favorites = () => {
  const [hero, setHero] = useState([]);

  useEffect(() => {
    const heroIdfromKey = Object.keys(localStorage).filter(
      (key) => localStorage.getItem(key) === "true"
    );

    const fetchCharacter = async () => {
      const heroData = heroIdfromKey.map(async (key) => {
        return await getCharacter(key);
      });
      setHero(await Promise.all(heroData));
    };

    fetchCharacter();
  }, []);

  const favoriteCharacters = hero.map((hero) => {
    return (
      <Card
        key={hero.id}
        name={hero.name}
        id={hero.id}
        tumbnail={`${hero.thumbnail.path}/${IMG_SIZE}.${hero.thumbnail.extension}`}
      />
    );
  });

  return (
    <>
      <Head>
        <title>MARVEL | Favorites</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/marvel.ico" />
      </Head>
      <div className="flex flex-col items-center justify-center">
        <h1 className="uppercase text-xl mb-6 text-center">
          Favorite Characters
        </h1>
        <Grid items={hero}>{favoriteCharacters}</Grid>
      </div>
    </>
  );
};

export default Favorites;
